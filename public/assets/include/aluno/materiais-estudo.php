<script>
    // Função para filtrar os cards com base na seleção do dropdown
    document.querySelector('.btn-login').addEventListener('click', function() {
        var selectedProfessor = document.getElementById('professor').value;

        // Obter todos os cards
        var cards = document.querySelectorAll('[data-professor]');

        // Iterar sobre todos os cards
        cards.forEach(function(card) {
            // Verificar se o card corresponde ao valor selecionado
            if (selectedProfessor === "" || card.getAttribute('data-professor') === selectedProfessor) {
                card.style.display = 'block'; // Exibir o card
            } else {
                card.style.display = 'none'; // Ocultar o card
            }
        });
    });
</script>